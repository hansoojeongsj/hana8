import NextAuth, { AuthError } from 'next-auth';
import Credentials from 'next-auth/providers/credentials';
import Github from 'next-auth/providers/github';
import Google from 'next-auth/providers/google';
import { prisma } from './prisma';
import { comparePassword } from './validator';

export const {
  handlers: { GET, POST },
  auth,
  signIn,
  signOut,
} = NextAuth({
  providers: [
    Credentials({
      name: 'Email',
      credentials: {
        email: { label: 'ì´ë©”ì¼', type: 'email', placeholder: 'user@mail.com' },
        passwd: {
          label: 'Password',
          type: 'password',
          placeholder: 'password...',
        },
      },
      async authorize(credentials) {
        console.log('ðŸš€ ~ credentials:', credentials);
        const { email, passwd } = credentials;
        return {
          email: email as string,
          passwd: passwd as string,
        };
      },
    }),
    Google,
    Github,
  ],
  callbacks: {
    async signIn({ user, account }) {
      console.log('ðŸš€ ~ account:', account);
      // console.log('ðŸš€ signIn - profile:', profile);
      console.log('ðŸš€ signIn - user:', user);
      const { email, passwd, name, image } = user;
      let oldUser =
        email && (await prisma.user.findUnique({ where: { email } }));
      console.log('ðŸš€ ~ oldUser:', oldUser);

      if (account?.provider === 'credentials') {
        if (!oldUser)
          throw makeAuthError('EmailSignInError', 'Not Exists Email');
        if (
          passwd &&
          oldUser.passwd &&
          !(await comparePassword(passwd, oldUser.passwd))
        )
          throw makeAuthError('EmailSignInError', 'Invalid Email or Password');
      } else {
        if (!oldUser) {
          if (!email || !name)
            throw makeAuthError('OAuthAccountNotLinked', 'Need email and name');

          oldUser = await prisma.user.create({
            data: { email, name, image },
          });
        }
      }

      user.id = String(oldUser.id);
      user.name = oldUser.name;
      user.image = oldUser.image;
      user.isadmin = oldUser.isadmin;

      return true;
    },
    async jwt({ token, user, trigger }) {
      // console.log('ðŸš€ jwt - token:', token);
      // console.log('ðŸš€ jwt - user:', user);
      if (trigger) console.log('ðŸš€ jwt - trigger:', trigger);
      if (user) {
        token.id = user.id;
        token.email = user.email;
        token.name = user.name;
        token.isadmin = user.isadmin;
      }
      return token;
    },
    async session({ session, user }) {
      if (user) {
        session.user.id = user.id;
        session.user.email = user.email;
        session.user.name = user.name;
        session.user.isadmin = user.isadmin;
      }
      return session;
    },
  },
  pages: {
    // signIn: '/sign',
    error: '/sign/error',
  },
  session: {
    strategy: 'jwt',
  },
  trustHost: true,
  jwt: { maxAge: 30 * 60 },
});

const makeAuthError = (type: AuthError['type'], message?: string) => {
  const err = new AuthError(message);
  err.type = type;
  return err;
};
